								<h2 class="major">Le jeu</h2>
								<ul class="alt">
                                    <h4>Le jeu</h4>
								    <li><p>Notre jeu se déroule dans un univers 3D de type "Tower Defense". La caméra sera dans un setup TPS et le gameplay se basera sur un hybride entre du Tower Defense et du combat direct. Cela signifie que notre héros sera dans une configuration de PvM (Player Vs Mobs) avec la possibilité de les combattre par un système de piège et de défense autonome, ici donc du Tower Defense pur, mais aussi la possibilité de combattre directement ces mobs, avec des capacités, éventuellement un inventaire et un arbre de compétence, ainsi que de prendre des dégâts, vulgarisable en beat them up.</p></li>
                                    <h4>L'histoire</h4>
                                    <li><p>Vous jouerez donc un elfe , fraîchement rejeté de l'académie d'ingénierie de la grande cité d’Argedhen, et qui, du haut de ses 70 ans vise les plus hautes sphères de l’armée. Hélas, enrôlé dans une guerre qui le dépasse, saura-t-il surmonter ses doutes et s’illustrer aux yeux de tous comme le fier descendant du Maréchal Thirquen et guider son peuple vers la victoire ? Ses connaissances en ingénieries vont être d’une aide précieuse pour reconstruire les bâtiments défensifs elfiques, mais cela sera-t-il suffisant ? Ce sera à vous d’en décider dans Elves Must Live.</p></li>
                                </ul>          
